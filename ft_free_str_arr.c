#include "ft_sh21.h"

void	ft_free_str_arr(char **arr)
{
	int		i;

	if (arr)
	{
		i = 0;
		while (arr[i] != NULL)
		{
			if (arr[i])
				free(arr[i]);
			arr[i] = NULL;
			i++;
		}
		if (arr)
			free(arr);
	}
}
