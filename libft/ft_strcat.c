#include "libft.h"

char	*ft_strcat(char *const s1, char const *s2)
{
	char	*s;
	int		i;

	s = s1;
	i = ft_strlen(s);
	while (*s2 != '\0')
	{
		s[i] = *s2++;
		i++;
	}
	s[i] = '\0';
	return (s1);
}
